# Given floating-point number
value = 12.34567

# Round to the nearest integer
rounded_nearest_integer = round(value)

# Round to two decimal places
rounded_two_decimal_places = round(value, 2)

# Print the results
print(f"Rounded to the nearest integer: {rounded_nearest_integer}")
print(f"Rounded to two decimal places: {rounded_two_decimal_places}")
